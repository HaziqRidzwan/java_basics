package java_basics;

public class Variables {

	public static void main(String[] args) {
		
		int x = 54;
		
		System.out.println(x);
		
		x = 47;
				
		System.out.println(x);
		
		System.out.println(x * 20);
		
		String words = "lmao";
		
		System.out.println(words);
		
		words = "this is a sentence";
		
		System.out.println(words);

	}

}
